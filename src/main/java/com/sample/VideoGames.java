package com.sample;

public class VideoGames {
	private String category;
	private String section;
	private String playersupport;
	private String nameofgame;
	private String paidgame;
	private String topseller;
	private String discount;
	private String mostplayed;
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	public String getPlayersupport() {
		return playersupport;
	}
	public void setPlayersupport(String playersupport) {
		this.playersupport = playersupport;
	}
	public String getNameofgame() {
		return nameofgame;
	}
	public void setNameofgame(String nameofgame) {
		this.nameofgame = nameofgame;
	}
	public String getPaidgame() {
		return paidgame;
	}
	public void setPaidgame(String string) {
		this.paidgame = string;
	}
	public String getTopseller() {
		return topseller;
	}
	public void setTopseller(String topseller) {
		this.topseller = topseller;
	}
	public String getDiscount() {
		return discount;
	}
	public void setDiscount(String discount) {
		this.discount = discount;
	}
	public String getMostplayed() {
		return mostplayed;
	}
	public void setMostplayed(String mostplayed) {
		this.mostplayed = mostplayed;
	}
	
    
	
}
